    public void capturaDatos(){
        nombre = txtNombre.getText();
        departamento = txtDepartamento.getText();
        descripcion = taDescripcion.getText();
        //hora = Calendar.getInstance().get(Calendar.HOUR_OF_DAY)+":"+ Calendar.getInstance().get(Calendar.MINUTE);
        hora = DateTimeFormatter.ofPattern("yyyy-MM-dd  HH:mm:ss").format(LocalDateTime.now());
        
    }
    
    public void mostrarDatos(){
        for(int i=0; i<datos.size(); i++){
            System.out.println(datos.get(i).getCodigo()+" " +" " + datos.get(i).getNombre()+ " "  + " " +  datos.get(i).getDepartamento() + " " + datos.get(i).getDescripcion() + " " + datos.get(i).getHora());
        }
        DefaultTableModel tableModel = new DefaultTableModel();
        String[] nombreColumnas = {"Codigo", "Nombre", "Departamento", "Descripcion", "Hora"};
        tableModel.setColumnIdentifiers(nombreColumnas);
        for(int i = 0; i<datos.size(); i++){
            tableModel.addRow(new Object[] {datos.get(i).getCodigo(),datos.get(i).getNombre(),datos.get(i).getDepartamento(), datos.get(i).getDescripcion(), datos.get(i).getHora()});
        }
        
        TableMostrar.setModel(tableModel);
    }

